        <h2 class="section-title">What is CorporaCamp?</h2>
        <p class="section-text">CorporaCamp is the first Bamboo <a href="cspace.php">Corpora Space</a> workshop. During the three-day event developers and scholars collaborated on a prototype scholarly application that works across three collections &mdash; <a href="http://www.lib.umich.edu/tcp/" target="_blank">TCP EEBO & ECCO</a>, <a href="http://www.hathitrust.org/home" target="_blank">HathiTrust</a>, and <a href="http://www.perseus.tufts.edu/hopper/" target="_blank">Perseus</a>. CorporaCamp involved three connected efforts - designing and developing the user interface, building the application, and creating the architecture of the platform. A lot of preparation was done before the three days by members of the <a href="cspace.php">Corpora Space</a> team to get started on the design of these three areas. The focus of this work was to begin realizing the goals of the <a href="cspace.php">Corpora Space</a> design process and to raise key questions about scholarly research on large collections of digital texts.</p>
