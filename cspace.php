        <h2 class="section-title">What is Corpora Space?</h2>
        <p class="section-text">Corpora Space is about collaboratively curating and exploring large-scale corpora.  By curation we mean &quot;the activity of managing and promoting the use of data from its point of creation, to ensure it is fit for contemporary purpose, and available for discovery and re-use.&quot; <a href="http://www.ukoln.ac.uk/ukoln/staff/e.j.lyon/150.pdf" target="_blank">(Lord 2004)</a> This involves both the enrichment and maintenance of content and metadata.  By exploration we mean discovery, analysis, visualization, and ways of generating new results and provoking new research questions. Curation is doing things <em>to</em> texts; exploration is doing things <em>with</em> texts.</p>
