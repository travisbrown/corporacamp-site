Woodchipper was built primarily to inform the <a href="cspace.php">Corpora Space</a> design process. Preparing for and carrying out <a href="ccamp.php">CorporaCamp</a> has given us insight into the problems and challenges we face in designing <a href="cspace.php">Corpora Space</a>. It has allowed us to build, test, and refine the application and the architecture on which it is built. The next step is to engage scholars in the design of <a href="cspace.php">Corpora Space</a> so that the tools we build will meet their needs. In order to do this we need your input, so please get involved by contacting us with feedback about the screencast, trying out the alpha version, and watching this space for the release of the beta version in the coming weeks.</p>
