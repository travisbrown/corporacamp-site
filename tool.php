        <p class="section-text"> <a href="images/woodchipper.jpg" target="_blank"><img src="images/woodchipper_t.jpg" alt="woodchipper" width="216" height="170" border="1" align="right" /></a>Weâ€™ve built a tool for visualization and analysis, code-named <strong>Woodchipper</strong>. If you really want to know, Woodchipper uses topic modeling and other dimensionality reduction techniques to reveal relationships between texts. The alpha version of Woodchipper currently allows a researcher to search across subsets of three collections, create a set of documents, and perform visualizations to show thematic proximity of members of the set. You can see a demonstration of it in action in the screencast, and if you would like to help us alpha test it, please complete the form found by clicking the green button at left. <br />
